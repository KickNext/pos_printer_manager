/// Слой Domain - бизнес-логика приложения.
///
/// Этот модуль содержит Use Cases, которые инкапсулируют
/// всю бизнес-логику приложения, обеспечивая чистое разделение
/// между слоями согласно принципам Clean Architecture.
///
/// ## Структура:
///
/// - `use_cases/` - Use Cases для каждой бизнес-операции
///
/// ## Принципы:
///
/// 1. **Use Cases не зависят от UI** - они работают только с
///    domain-объектами и абстракциями репозиториев.
///
/// 2. **Каждый Use Case - одна операция** - принцип единственной
///    ответственности.
///
/// 3. **Result вместо исключений** - все Use Cases возвращают
///    `Result<T>` для типобезопасной обработки ошибок.
///
/// 4. **Логирование через LoggerMixin** - единообразное
///    структурированное логирование.
